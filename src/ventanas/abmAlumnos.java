    private void btnModificarbtnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarbtnCargarActionPerformed
        if ( evt.getSource()== btnCargar ){
            jPBorrar.setVisible(false);
            jPCargar.setVisible(true);
            jPModificar.setVisible(false);
        }
        else if ( evt.getSource()== btnBorrar ){
            jPBorrar.setVisible(true);
            jPCargar.setVisible(false);
            jPModificar.setVisible(false);
        }
        else if ( evt.getSource()== btnModificar){
            jPBorrar.setVisible(false);
            jPCargar.setVisible(false);
            jPModificar.setVisible(true);
            List<Alumno> alumnos = alumnoData.obtenerAlumnos();
            boolean control;
            for(Alumno alumno : alumnos){
                control=true;
                for(int i=0;i<cbAlumnoModificar.getItemCount();i++){
                    if(Integer.parseInt(cbAlumnoModificar.getItemAt(i)) == alumno.getId()){
                        control=false;
                    }
                }
                if(control){
                    cbAlumnoModificar.addItem(String.valueOf(alumno.getId()));
                }
            }
        }
    }//GEN-LAST:event_btnModificarbtnCargarActionPerformed

    private void btnBorrarbtnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarbtnCargarActionPerformed
        if ( evt.getSource()== btnCargar ){
            jPBorrar.setVisible(false);
            jPCargar.setVisible(true);
            jPModificar.setVisible(false);
        }
        else if ( evt.getSource()== btnBorrar ){
            jPBorrar.setVisible(true);
            jPCargar.setVisible(false);
            jPModificar.setVisible(false);
            List<Alumno> alumnos = alumnoData.obtenerAlumnos();
            boolean control;
            for(Alumno alumno : alumnos){
                control=true;
                for(int i=0;i<cbListaAlumnos.getItemCount();i++){
                    if(Integer.parseInt(cbListaAlumnos.getItemAt(i)) == alumno.getId()){
                        control=false;
                    }
                }
                if(control){
                    cbListaAlumnos.addItem(String.valueOf(alumno.getId()));
                }
            }
            rbID.setSelected(false);
            cbListaAlumnos.setEnabled(true);
            tfBuscarId.setEnabled(false);
            jbBuscar.setEnabled(false);
        }
        else if ( evt.getSource()== btnModificar){
            jPBorrar.setVisible(false);
            jPCargar.setVisible(false);
            jPModificar.setVisible(true);
        }
    }//GEN-LAST:event_btnBorrarbtnCargarActionPerformed

