    private void cbAlumnoModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbAlumnoModificarActionPerformed
        jtMostrarNombre.setText(alumnoData.buscarAlumno(Integer.parseInt(cbAlumnoModificar.getSelectedItem().toString())).getNombre_completo());
        jtMostrarFechaNac.setText(alumnoData.buscarAlumno(Integer.parseInt(cbAlumnoModificar.getSelectedItem().toString())).getNacimiento_alumno().toString());
        if(alumnoData.buscarAlumno(Integer.parseInt(cbAlumnoModificar.getSelectedItem().toString())).isActivo()){
            btnActivo.setText("Activo");
        }
        else
            btnActivo.setText("Inactivo");
    }//GEN-LAST:event_cbAlumnoModificarActionPerformed

